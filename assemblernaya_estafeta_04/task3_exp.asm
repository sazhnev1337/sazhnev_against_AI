; Задание 3: вычислить e^x для x ∈ float16, хранящегося по адресу [A].
; Результат — float16 — записать в res по адресу [B].
;
; Метод: разложение в ряд Тейлора вокруг 0:
;   e^x = Σ_{k=0}^{N} x^k / k!
;       = 1 + x + x²/2! + x³/3! + ... (N члeнов)
;
; Для float16 (мантисса ~10 бит, ~3.3 decimal digits) достаточно N=8 членов
; при |x| ≤ 1. При больших x точность ограничена форматом.
;
; Реализация итеративным методом Хорнера (вычисляем обратным порядком):
;   Хорнер: e^x ≈ 1 + x*(1 + x*(1/2 + x*(1/6 + x*(1/24 + x*(1/120
;                    + x*(1/720 + x*(1/5040 + x*(1/40320))))))))
;   Итого 8 членов (k=0..7 плюс остаточный член k=8 даёт 9 коэффициентов).
;
; Коэффициенты 1/k! в float16 (k = 8 вниз до 0), хранятся в памяти:
;   coeff[0] = 1/8! = 1/40320 ≈ 2.48e-5_h
;   coeff[1] = 1/7! = 1/5040  ≈ 1.984e-4_h
;   coeff[2] = 1/6! = 1/720   ≈ 1.389e-3_h
;   coeff[3] = 1/5! = 1/120   ≈ 8.333e-3_h
;   coeff[4] = 1/4! = 1/24    ≈ 4.167e-2_h
;   coeff[5] = 1/3! = 1/6     ≈ 0.16667_h
;   coeff[6] = 1/2! = 1/2     = 0.5_h
;   coeff[7] = 1/1! = 1       = 1.0_h
;   coeff[8] = 1/0! = 1       = 1.0_h  (свободный член)
;
; Схема Хорнера (итерация, N=8):
;   result = coeff[0]
;   for i = 1..8:
;       result = result * x + coeff[i]
;
; Используем:
;   FHA — текущий result (float16)
;   FHB — x (float16), загружен один раз
;   FHC — текущий coeff[i] (float16)
;   A   — адрес x / указатель на текущий коэффициент
;   B   — адрес res
;   C   — счётчик итераций (8 штук)

JMP start

x:   DB 1.0_h

res: DB 0.0_h

; Коэффициенты 1/k! от k=8 до k=0 (порядок применения в схеме Хорнера)
coeffs: DB 0.000024796_h   ; 1/8! = 1/40320
        DB 0.000198413_h   ; 1/7! = 1/5040
        DB 0.001388889_h   ; 1/6! = 1/720
        DB 0.008333333_h   ; 1/5! = 1/120
        DB 0.041666667_h   ; 1/4! = 1/24
        DB 0.166666667_h   ; 1/3! = 1/6
        DB 0.5_h           ; 1/2! = 1/2
        DB 1.0_h           ; 1/1! = 1
        DB 1.0_h           ; 1/0! = 1  (свободный член)

start:
    MOV A, x
    MOV B, res

    ; Загружаем x в FHB (останется там всё время)
    FMOV.H FHA, [A]
    FMOV.H FHB, FHA         ; FHB = x (сохраняем копию)

    ; Загружаем первый коэффициент 1/8! → FHA (= result начальное)
    MOV A, coeffs
    FMOV.H FHA, [A]         ; FHA = 1/8!

    ; Счётчик итераций: 8 умножений
    MOV C, 8

.horner_loop:
    ; result = result * x
    FMUL.H FHA, FHB

    ; Загружаем следующий коэффициент
    INC A
    INC A                   ; +2 байта (float16)
    FMOV.H FHC, [A]         ; FHC = coeff[i]

    ; result = result + coeff[i]
    FADD.H FHA, FHC

    DEC C
    CMP C, 0
    JNZ .horner_loop

    ; Записываем результат в res
    FMOV.H [B], FHA

    HLT
